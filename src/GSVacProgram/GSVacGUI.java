    private void PriorityBTNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PriorityBTNMouseClicked
        for (int i = 0; i < n.size(); i++) {
            Patient p = new Patient(); n.size(); 
            p.setName(n.get(i));
            p.setAge(a.get(i));
            p.setUl(u.get(i));

            String uli = u.get(i);

            String zx = a.get(i);
            int z = Integer.parseInt(zx);
            int pr = 0;

            if (z > 90) {
                pr = 10;
            }
            if (z > 80) {
                pr = 9;
            }
            if (z > 70) {
                pr = 8;
            }
            if (z >= 65 && z <= 69) {
                pr = 7;
            }

            if (z >= 55 && z <= 64) {
                pr = 5;
            }
            if (z >= 45 && z <= 54) {
                pr = 4;
            }
            if (z >= 30 && z <= 44) {
                pr = 3;
            }
            if (z >= 18 && z <= 29) {
                pr = 2;
            }

            if (z < 18) {
                pr = 1;
            }
            if (z >= 18 && z <= 64) {
                if (uli.equalsIgnoreCase("yes")) {
                    pr = 6;
                }
            }
            myPQueue.enqueue(pr, p);
        }

// TODO add your handling code here:
    }//GEN-LAST:event_PriorityBTNMouseClicked


