    private void VacBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VacBTNActionPerformed
        // TODO add your handling code here:

        if (!myPQueue.isEmpty()) {
            PQElement pqelem = (PQElement) myPQueue.dequeue();
            Patient p1 = pqelem.getElement();

            textArea.append(" Next to be Vacinated is " + p1.getName());
            textArea.append("  Age: " + p1.getAge());
            textArea.append("  Priority: " + pqelem.getKey() + "\n");
        } else {
            textArea.append("There are no more to be vacinated" + "\n");
        }
    }//GEN-LAST:event_VacBTNActionPerformed

